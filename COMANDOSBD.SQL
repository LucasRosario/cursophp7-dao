CREATE TABLE EQUIPAMENTOS(
	IDEQUIPAMENTOS INT PRIMARY KEY AUTO_INCREMENT,
	SETOR VARCHAR(30),
	MARCA VARCHAR(20),
	PROJETO VARCHAR(20) ,
	TIPO VARCHAR(20) NOT NULL,
	TOMBO INT NOT NULL
);

CREATE TABLE CPU(
	IDCPU INT PRIMARY KEY AUTO_INCREMENT,
	N_SERIE CHAR(7) NOT NULL,
	PROCESSADOR CHAR(2),
	OFFICE_2016 CHAR(29),
	CONTA VARCHAR(30) NOT NULL,
	GARANTIA CHAR(10),
	USUARIO VARCHAR(20),
	ID_EQUIPAMENTOS INT UNIQUE,
	FOREIGN KEY(ID_EQUIPAMENTOS) REFERENCES EQUIPAMENTOS(IDEQUIPAMENTOS)
);


CREATE TABLE USUARIOS(
	ID_USUARIOS INT AUTO_INCREMENT PRIMARY KEY,
	LOGIN VARCHAR(35) NOT NULL UNIQUE,
